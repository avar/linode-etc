        content_like => qr/Hailo is a a pluggable/,
        base_url     => "http://hailo.github.com/",
    },
    'hailo.org'                => {
        content_like => qr/Hailo is a a pluggable/,
        base_url     => "http://hailo.github.com/",
    },
